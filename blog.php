                American Seed in Junction, Texas, sells native seeds and seed mixes designed to be an “ecosystem in a
                bag.” <a
                    href='https://native-american-seed6.mybigcommerce.com/'>https://native-american-seed6.mybigcommerce.com/</a>
                For example, non-native turf can be replaced with their “Thunder Turf.” There is even a “Pocket Prairie
                Mix” specifically designed to increase biodiversity in urban landscapes. Those who do not have a yard
                can still help restore lost habitat by following Homegrown National Park’s “Container Gardening With
                Keystone Plants” guide: <a
                    href='https://homegrownnationalpark.org/keystone-container-gardening?gclid=Cj0KCQjwtsCgBhDEARIsAE7RYh1DkLJ9m2fxUapyqkUDUfWjqdsEZkWfxAy9Hea2FxK_Gl5OtaRLgzEaAio1EALw_wcB'>https://homegrownnationalpark.org/keystone-container-gardening?gclid=Cj0KCQjwtsCgBhDEARIsAE7RYh1DkLJ9m2fxUapyqkUDUfWjqdsEZkWfxAy9Hea2FxK_Gl5OtaRLgzEaAio1EALw_wcB</a>.
