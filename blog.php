                    open water, Bat Conservation International provides guidance for providing water troughs with
                    wildlife escape structures</b>: <a
                    href='https://www.batcon.org/wp-content/uploads/bciwaterforwildlife.pdf'>https://www.batcon.org/wp-content/uploads/bciwaterforwildlife.pdf</a>.
            </p>
