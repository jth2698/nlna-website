                <br>
                <div>
                    <p><i>Climate change can be daunting. Even if individuals cannot personally solve the crisis, they
                            can
                            choose not to contribute to the problem by a “do no harm” approach in whatever balcony,
                            patio or
                            yard they can control. The quote often attributed to Mahatma Gandhi, “Be the change you wish
                            to
                            see in the world,” is relevant even here in our North Loop Neighborhood.</i></p>
                    <p><i>This column focuses on concrete, timely and frugal measures to support the struggling
                            pollinators
                            and wildlife that are also a part of our urban density. Many of these creatures are
                            suffering
                            from habitat loss, pesticide, pollution and extreme weather. Compassionate urban land
                            stewardship matters to our native flora and fauna.</i></p>
                    <p>January is a good time to take care of work that might otherwise interfere with wildlife or risk
                        tree
                        health in the spring/summer. For example, is there necessary certified arborist work that might
                        be
                        accomplished now (before birds build their nests starting as early as next month)? January is
                        regarded as the last safe month to prune oak trees (always painting pruning cuts) <a
                            href="https://www.austintexas.gov/page/oak-wilt">before the risk of Oak Wilt increases</a>.
                        If
                        trees suffered severe damage during last summer’s drought, <a
                            href="https://cavityconservation.com/before-removing-a-dead-tree-check-for-birds-nesting-in-cavities/">might
                            they still safely provide precious habitat?</a></p>
                    <p>January is also a good time to prevent germination of spring weeds without harming the
                        environment.<a href="https://tngaustin.com/january/">The Natural Gardener recommends purchasing
                            corn gluten
                            early this month</a> to apply to lawn or garden beds later as soon as sunny days return.</p>
