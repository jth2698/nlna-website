                                                <h5 class="text-center" id="June_2022">June, 2022</h5>
                                                <hr width="25%">
                                                </hr>
                                                <h3 class="text-center"><b>Orange Line Plans to bring change to North
                                                        Lamar</b></h3>
                                                <br>
                                                <div class="row-md-3">
                                                    <div class="col-md-5 mx-auto" style="width: 90%">
                                                        <img src="https://northloopatx.org/images/north-lamar.png"
                                                            class="img-fluid" alt="North Lamar Street View">
                                                    </div>
                                                </div>
                                                <br>
                                                <p>In April 2022, Project Connect shared <a
                                                        href='https://publicinput.com/64715'>updated draft
                                                        designs</a>
                                                    for
                                                    the
                                                    Orange Line project along North Lamar,
                                                    including sections serving the North Loop neighborhood. The project
                                                    will
                                                    provide
                                                    new or improved
                                                    transportation
                                                    amenities, including light rail, bike lanes, and better sidewalks.
                                                    The
                                                    Orange
                                                    Line will become an
                                                    essential
                                                    transit corridor from North Austin to UT, downtown, south Austin,
                                                    and
                                                    Blue Line
                                                    connections to the
                                                    airport.
                                                    Light
                                                    rail transit and transportation corridor development will help
                                                    address
                                                    the
                                                    city’s growing
                                                    transportation
                                                    needs,
                                                    housing shortages, and greenhouse gas reduction goals.</p>
                                                <p>Koenig Station, a light rail stop on north Lamar Boulevard near
                                                    Half-Price Books,
                                                    will serve our
                                                    area.
                                                    The
                                                    draft
                                                    design for the station provides for dedicated sidewalks, bike lanes,
                                                    and
                                                    urban
                                                    tree zones on both
                                                    sides
                                                    of
                                                    the
                                                    boulevard; four vehicle traffic lanes and a left-turn lane
                                                    approaching
                                                    Koenig
                                                    Lane; and, a
                                                    center-located
                                                    station
                                                    platform. The crosswalk on Lamar on the northside of Houston Street
                                                    will
                                                    provide
                                                    pedestrian access to
                                                    the
                                                    south
                                                    side of the station platform. A new crosswalk and traffic beacon to
                                                    be
                                                    placed
                                                    across Lamar near Dan’s
                                                    Hamburgers
                                                    and KFC will provide station access on the northside.</p>
                                                <p>District 4 Council Member Jose “Chito” Vela told North Loop
                                                    Neighborhood
                                                    Association members on June
                                                    1
                                                    that
                                                    he
                                                    is
                                                    asking the city to bury the utilities currently suspended adjacent
                                                    to
                                                    North
                                                    Lamar. This change would
                                                    allow
                                                    the
                                                    new
                                                    space to more closely resemble a tree-lined urban boulevard, as well
                                                    as
                                                    removing
                                                    potential obstacles
                                                    on
                                                    sidewalks
                                                    and bike lanes.</p>
                                                <p>The Orange Line project is progressing through the engineering and
                                                    design
                                                    phases,
                                                    which include
                                                    incorporating
                                                    input from the community as well as addressing technical and
                                                    budgetary
                                                    issues.
                                                    This process involves
                                                    analysis
                                                    and
                                                    trade-off decisions on goals such as maximizing project benefits,
                                                    while
                                                    minimizing the impacts on
                                                    adjacent
                                                    private
                                                    property, and controlling costs. This approach is important for
                                                    sections
                                                    where
                                                    the existing roadway
                                                    and
                                                    public
                                                    easements are not wide enough to accommodate the full range of
                                                    planned
                                                    features.
                                                </p>
                                                <p>In their April 18, 2022, update, project officials identified
                                                    multiple
                                                    design
                                                    issues found in our
                                                    area
                                                    as
                                                    well
                                                    as
                                                    proposed solutions to address them. To manage space constraints in
                                                    some
                                                    areas,
                                                    the design would
                                                    eliminate
                                                    tree
                                                    zones and/or combine the bicycle and pedestrian facilities into a
                                                    shared
                                                    path.
                                                    More significantly, to
                                                    avoid
                                                    impacting a large residential building like the Camden Lamar Heights
                                                    Apartments,
                                                    the design would
                                                    shift
                                                    project
                                                    lanes beyond the current east roadway curb on Lamar. As a result,
                                                    the
                                                    design
                                                    would impact space
                                                    currently
                                                    used
                                                    by
                                                    North Loop businesses from the Texaco Station to the KFC. The
                                                    impacts in
                                                    most
                                                    cases would be limited
                                                    to
                                                    areas
                                                    currently used for driveways, parking, and sidewalks. However, in a
                                                    few
                                                    cases,
                                                    the design drawings
                                                    reveal
                                                    possible
                                                    impacts with a building or other structures.</p>
                                                <p>Project officials said that they have discussed the project’s
                                                    potential
                                                    impacts
                                                    on businesses located
                                                    along
                                                    the
                                                    route. However, they also said that if any properties along the
                                                    route
                                                    have not
                                                    been contacted, they
                                                    would
                                                    like
                                                    to
                                                    hear from them. Project plans and designs are subject to changes
                                                    during
                                                    the
                                                    design and approval
                                                    processes.
                                                    Project
                                                    Connect is scheduled to release its 30% design plans and
                                                    environmental
                                                    impact
                                                    statement this summer.
                                                </p>
                                                <p>NLNA will continue to monitor and share information on the Orange
                                                    Line
                                                    throughout
                                                    the project. For
                                                    additional
                                                    information on project plans and schedules, upcoming public
                                                    meetings,
                                                    and
                                                    contact information, visit
                                                    <a href='https://projectconnect.com'>projectconnect.com</a>. To
                                                    share
                                                    information or concerns about
                                                    the
                                                    Orange
                                                    Line
                                                    with NLNA, or to volunteer to participate in monitoring the project,
                                                    <a href='https://northloopatx.org/contact'>contact us</a>. The
                                                    Orange
                                                    Line is
                                                    not projected to begin
                                                    service
                                                    before
                                                    2028.
                                                </p>
