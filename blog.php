                    <h3 class="text-center"><b>North Loop Therapist Offers Tips for Navigating Holiday Stress</b></h3>
                    <div class="row-md-3">
                        <div class="col-md-6 mx-auto" style="width: 90%">
                            <img src="https://northloopatx.org/images/bruning-green.png" class="img-fluid"
                                alt="Bruning Green">
                        </div>
                    </div>
                    <br>
                    <div>
                        <p>Spending time in nature is such a good thing for mental health. Taking a hike through the
                            woods
                            can
                            improve your mood, help lower anxiety and depression, increase motivation, improve your
                            immune
                            system
                            and also help you concentrate better. Getting outside and away from technology helps us to
                            connect back
                            to our roots, get grounded, and take a breath of fresh air. Spending time in nature helps us
                            to
                            be
                            mindful and more present in our own lives.</p>
                        <p>Ecotherapy is a type of therapy that involves spending time in nature with a trained
