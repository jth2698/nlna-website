        <div>
            <p>The meteorological summer has ended, but the Autumnal Equinox remains weeks away and our drought has
                deepened. <b>Considering how difficult it is for newly planted trees and shrubs to survive increasingly
                    extreme weather, it makes good sense to preserve already established native and adapted
                    vegetation.</b> Now that we have lost a <a
                            href='https://www.austintexas.gov/news/austin-enters-stage-2-drought-contingency-plan'>watering
                            day</a>, it is worth exploring <a
                            href='https://treegator.com/products/original/install/double_bag.html'>simple tools to
                            sustain the precious tree canopy that offsets our urban heat island</a>.
            </p>
            <p><b>You can help birds right now by turning off all non-essential outdoor lighting between 11 p.m. and 6
                    a.m. <a href='https://travisaudubon.org/lights-out-texas'>because fall migration began last month
                        and is expected to continue through November 30, with peak night migration from September 6
                        through October 29</a>.</b> At our last neighborhood association meeting, we learned about the
                        importance of dark skies for the health of so many creatures, including people! <a
                            href='https://darksky.org/what-we-do/darksky-approved/products-companies/'>DarkSky
                            International’s website</a> can be searched for products that meet their standards to combat
                        light pollution.
            </p>
            <p><b>Migrating birds that have survived flying through the entire night face yet another hazard in our
                    neighborhoods: colliding with glass.</b> American Bird Conservancy <a
                            href='https://abcbirds.org/blog/truth-about-birds-and-glass-collisions/'>explains</a> that:
                        “migratory birds that have flown all night are usually in a state of exhaustion as the sun comes
                        up and as they look for a place to land and refuel. Those that land in and near cities find
                        themselves in a maze of deadly glass…Fall is worse than spring due to the larger number of birds
                        in flight. This is because fall migration includes both adult birds and juveniles that were born
                        over the summer.” There are, however, <a
                            href='https://windowalert.com/windowalert-products/'>inexpensive and unobtrusive
                            products</a> that compassionate owners and renters can use to protect exhausted birds that
                        are unfamiliar with our area.
            </p>
        </div>
