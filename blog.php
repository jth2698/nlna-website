            <p>Bee City U.S.A. advocates for “No Mow May, Low Mow Spring” because less-frequently mowed lawns support
                greater numbers and diversity of bees and butterflies. They point out that: “Lawns cover 40 million
                acres, or 2%, of land in the US, making them the single largest irrigated crop we grow…When we think of
                habitat loss, we tend to imagine bulldozer and rutted earth, but acres of manicured lawn are as much a
                loss of habitat as any development site.” <a
                    href='https://beecityusa.org/no-mow-may/#'>https://beecityusa.org/no-mow-may/#</a>
            </p>
            <p>The City of Austin’s website includes “Grass/Weeds Over 12 Inches” on its list of “Five common Code
                violations.” <a
                    href='https://www.austintexas.gov/department/common-austin-code-violations#tallgrass'>https://www.austintexas.gov/department/common-austin-code-violations#tallgrass</a>.
                PennState Extension explores the history of Noxious Weed Laws, however, and debunks “myths” that natural
                landscapes: are fire hazards, attract vermin, harbor ticks, breed mosquitos, worsen allergies and reduce
