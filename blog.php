            <p><b>Migrating birds need our help right now because urban light pollution is a hazard</b>. “Travis Audubon
                and a coalition of other local and state organizations want us to flip the switch for birds this spring!
                Nearly two billion birds migrate across Central Texas skies each spring and fall, and many of them fly
                at night. Spring migration is already underway and we want to make their journey as safe as possible.
                Artificial lighting can cause birds to be disoriented and collide with buildings, and these avoidable
                collisions kill up to one billion migratory birds annually in the United States. The good news is that
                everyone can help. <b>Let’s turn off all nonessential indoor and outdoor lighting from 11 p.m. to 6 a.m.
                    March 1–June 15, but especially during peak migration dates of April 22–May 12.</b> Please shut off
                porch and garage lights, close your curtains and shades, and ask your office building management to shut
                off their lights at night, too. You’ll be protecting birds while also saving money on energy. Learn more
                at <a href='https://travisaudubon.org/lights-out-texas'>https://travisaudubon.org/lights-out-texas</a>
