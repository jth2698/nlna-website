                    href='https://extension.psu.edu/neighborly-natural-landscaping-in-residential-areas'>https://extension.psu.edu/neighborly-natural-landscaping-in-residential-areas</a>.
                Noting the “ongoing issue of municipalities misunderstanding the benefit of native plantings and their
