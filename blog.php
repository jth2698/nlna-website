                door”: <a
                    href='https://wildones.org/native-plant-ordinance/'>https://wildones.org/native-plant-ordinance/.</a>.
            </p>
            <p>Yard equipment directly and significantly contributes to climate change. Environment Texas cites
                staggering national statistics that gas-powered lawnmowers produce five percent of total U.S. pollution,
                and Americans use three billion gallons of gasoline for yard maintenance (while spilling another 17
                million gallons during refueling): <a
                    href='https://environmentamerica.org/texas/articles/pollution-from-leaf-blowers-and-lawn-equipment/'>https://environmentamerica.org/texas/articles/pollution-from-leaf-blowers-and-lawn-equipment/.</a>
