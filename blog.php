                                                <h3 class="text-center"><b>As part of a new climate initiative, North
                                                        Loop
                                                        adopts
                                                        Waller Creek</b></h3>
                                                <p>Just in time for <a
                                                        href='https://www.austintexas.gov/blog/celebrate-earth-day-everyday-our-earth-month-calendar'>Earth
                                                        Month</a>,
                                                    the
                                                    NLNA has adopted our stretch of Waller Creek between North Loop and
                                                    Koenig Lane.
                                                    The non-profit Keep
                                                    Austin
                                                    Beautiful and the City of Austin’s Watershed Protection Division
                                                    support
                                                    Austin’s <a
                                                        href='https://keepaustinbeautiful.org/adopt-a-creek-2/'>Adopt-a-Creek
                                                        program</a>. </p>
                                                <p>Under this two-year adoption, NLNA will conduct at least four creek
                                                    events per
                                                    year. In addition to
                                                    continuing
                                                    our
                                                    trash pick-ups, the neighborhood will engage in environmental
                                                    assessment
                                                    and
                                                    habitat restoration, when
                                                    appropriate.
                                                    Our City and non-profit partners will provide a tool-lending library
                                                    as
                                                    well as
                                                    expert guidance to
                                                    ensure
                                                    that
                                                    our
                                                    actions benefit both our neighbors and the health of the creek.</p>
                                                <p>The creek adoption represents the latest in a series of long-term
                                                    efforts
                                                    to
                                                    enhance the benefits of
                                                    the
                                                    creek
                                                    to
                                                    the neighborhood. The <a
                                                        href='https://www.austintexas.gov/sites/default/files/files/Housing_%26_Planning/Adopted%20Neighborhood%20Planning%20Areas/21_NorthLoop/nloop-np.pdf'>2002
                                                        North Loop Neighborhood Plan</a> called for the city to
                                                    construct
                                                    and
                                                    promote water quality and
                                                    flood
                                                    controls
                                                    for
                                                    the creek. The plan also called for recurring clean-ups of the creek
                                                    and
                                                    for
                                                    establishing a hike and
                                                    bike
                                                    trail
                                                    parallel to the creek along Chesterfield Ave. Throughout the years,
                                                    residents
                                                    have conducted periodic
                                                    clean-ups
                                                    and
                                                    planted shrubs and wildflowers in parts of the creek.</p>
                                                <p>Waller Creek is a valuable resource for our (currently park
                                                    deficient)
                                                    neighborhood that allows us to
                                                    connect
                                                    with
                                                    greenspace while also enjoying our urban amenities. Please join us
                                                    in
                                                    helping to
                                                    protect the creek and
                                                    ensuring
                                                    it
                                                    continues to live up to its potential. </p>
