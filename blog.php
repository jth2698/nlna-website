            <p>Not all pollinators are graceful colorful butterflies or cute fuzzy bees, and some are admittedly
                challenging co-residents of our yards. Nonetheless, <b>less celebrated creatures like wasps are indeed
                    pollinators who also perform valuable pest control functions, and they too are hurting from climate
                    change</b>. <a
                    href=' https://www.ucl.ac.uk/news/2021/apr/wasps-are-valuable-ecosystems-economy-and-human-health-just-bees'>
                    https://www.ucl.ac.uk/news/2021/apr/wasps-are-valuable-ecosystems-economy-and-human-health-just-bees</a>
            </p>
            <p><b>Rather than indiscriminately killing these beneficial insects, one might carefully identify the
                    species and assess risk to exercise tolerance and mercy whenever possible.</b> For example, the
                Northwest Center for Alternatives to Pesticides notes in “Living with Bees & Wasps” that native paper
                wasps “can be left alone in many cases,” and that mud daubers have been described as “extremely docile”:
                <a href='https://www.pesticide.org/bees_wasps'>https://www.pesticide.org/bees_wasps</a>. Even if a nest
                is deemed an actual safety hazard, ethical home environmental stewardship might involve considerations
                of time of day and season (to reduce numeric impact) as well as non-chemical removal.
            </p>
            <p>On the subject of creatures affected by our home pesticide decisions, the nocturnal chirping you hear may
                not be crickets, but, rather, tiny insect-eating amphibians: <a
                    href='https://biodiversity.utexas.edu/news/entry/cliff-chirping-frog'>https://biodiversity.utexas.edu/news/entry/cliff-chirping-frog</a>.
            </p>
