        <h3 class="text-center"><b>April 2023: Your Urban Wildlife Habitat Matters to Climate Change!</b></h3>
        <div class="row-md-3">
            <div class="col-md-6 mx-auto" style="width: 90%">
                <img src="https://northloopatx.org/images/bee-on-flower.jpg" class="img-fluid" alt="Bee on a Flower">
            </div>
        </div>
        <br>
        <div>
            <p>Birds are already nesting in our yards. The U.S. Fish & Wildlife Service states that: “Most bird nests
                are protected under the Migratory Bird Treaty Act (MBTA). This law says: ‘No person may take (kill),
                possess, import, export, transport, sell, purchase, barter, or offer for sale, any migratory bird, or
                the parts, nests, or eggs of such bird except as may be permitted under the terms of a valid permit…’
                Under the MBTA it is illegal to destroy a nest that has eggs or chicks in it or if there are young birds
                that are still dependent on the nest for survival.” <a
                    href='https://www.fws.gov/story/bird-nests'>https://www.fws.gov/story/bird-nests</a>
            </p>
            <p>There are also baby mammals in our yards right now. Austin Wildlife Rescue (AWR) shares that well-meaning
                people can inadvertently “rescue” a mammal or bird that does not actually need to be rescued (and can be
                better cared for by its parents). They have an excellent flow chart explaining what to do if you find a
                baby bird or baby mammal: <a
                    href='https://www.austinwildliferescue.org/foundawildanimal'>https://www.austinwildliferescue.org/foundawildanimal</a>.
            </p>
            <p>One mammal deserves special mention, and that is North America’s only marsupial, the opossum. During our
                Neighborhood Association meeting last month, we learned from AWR that opossums control our local tick
                populations. They also eat rats and cockroaches. They are transient, unsung backyard heroes, and they
                are sometimes struck by cars. Recently killed mother opossums may still have live young in their
                pouches. If you see an opossum on the road, the Opossum Society of the United States suggests that: “If
                safe to do so, pull over and move the body to the side of the road. If alive then seek immediate
                assistance. If it is a deceased female then check the pouch for any live young. If found then wrap in a
                soft, ravel-free towel and transport to a wildlife rehabilitator immediately. Young opossums cannot
                survive long outside the pouch environment.” <a
                    href='https://opossumsocietyus.org/faq-opossum/#I%20saw%20an%20opossum%20on%20the%20road'>https://opossumsocietyus.org/faq-opossum/#I%20saw%20an%20opossum%20on%20the%20road</a>
            </p>
            <p>Finally, now is the time to properly mulch trees (using natural materials in a wide, flat circle and
                never “volcano mulching”) to prepare them before our summer heat. Earth Day can be observed all month,
                and “TreeFolks has named Earth Month ‘Mulch Month’ because that is the very best thing you can do for
                your trees right now”: <a
                    href='https://www.treefolks.org/2023/03/28/mulch-month-2023/'>https://www.treefolks.org/2023/03/28/mulch-month-2023/</a>.
            </p>
