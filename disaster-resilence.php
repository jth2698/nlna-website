    <h3 class="pb-3">Household Disaster Preparedness</h3>
    <h5>Make a Plan</h5>
    <p>Planning involves developing an understanding of the significant risks that natural disasters could pose to your
      housing, your household members, and your pets. Is your housing in or near the floodplain or poorly-insulated, for
      example? Do you have children or household members with special medical needs or limitations? In what
      circumstances should you shelter-in-place and in what type of events should you plan to evacuate? What can you do
      to make your home more resilient to natural disaster threats? (The table below contains links to additional
      information on resources on emergency preparedness.)</p>
    <h5>Build a Kit</h5>
    <p>Once you have completed some planning, you should consider building two “kits.” The first kit is to ensure that
      your household has enough supplies on hand to shelter in place for several days. The second “kit” is to ensure
      your household has items it would need if you had to evacuate to another location for safety. </p>
    <h5>Know your Neighbors</h5>
    <p>Natural disasters can potentially affect the entire region, impacting the ability of the City of Austin to
      respond timely to some requests for assistance. Knowing your neighbors and identifying those who might need extra
      help during a disaster is critical to increasing Austin’s disaster preparedness. If you can help one of your
      neighbors, that could be one less emergency call that needs to be made.</p>
    <h5>Stay Informed</h5>
    <p>Download the Ready Central Texas app on your mobile phone and/or sign up for emergency notifications to your
      telephone, email, or text messages.</p>
