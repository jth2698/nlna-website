      <h3 class="pt-2 pb-2">Local Natural Disasters</h3>
      <p>Central Texas has always been vulnerable to certain natural disasters. However, climate change has the
        potential to increase the frequency and impacts of threats from extreme weather. The city of Austin has
        identified the following as the top natural disasters Austinites face:</p>
      <ul><strong>Flooding</strong>–Austin is prone to flash flooding during heavy rains, especially along its urban streams, including
        near Waller Creek in the North Loop neighborhood.</ul>
      <ul><strong>Wildfire</strong>–The combination of Austin’s abundant natural vegetation, high temperatures, and frequent drought
        exposes Austin to risks of wildfires. </ul>
      <ul><strong>Severe Weather</strong>–Austin is experiencing an increase in extreme weather patterns, from the persistent lows of
        2021’s Winter Storm Uri to increased summer highs exceeding 100 degrees. On March 21, 2022, six tornadoes
        occurred in Central Texas.</ul>
