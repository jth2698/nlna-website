    <h3 class="pb-3">How the City of Austin Prepares for and Responds to Disasters</h3>
    <p>The City of Austin coordinates with multiple organizations, government and private, in planning for and
      responding to natural disasters. An organization responsible for coordinating emergency responses in Austin is the
      city’s <a href="https://www.austintexas.gov/department/homeland-security-and-emergency-management">Office of
        Homeland and Security Management</a>. Not surprisingly, the city relies heavily on its routine operational
      functions, such as <a href="https://austinenergy.com/ae/">Austin Energy</a>, <a
        href="https://www.austintexas.gov/department/watershed-protection">Watershed Protection</a>, and the <a
        href="https://www.austintexas.gov/department/fire">Fire Department</a> to respond to natural threats. Due to the
      importance of climate change in increasing risks for the city, the <a
        href="https://www.austintexas.gov/department/sustainability">Office of Sustainability</a> plays an increasing
      role in preparing for weather-related challenges.</p>
