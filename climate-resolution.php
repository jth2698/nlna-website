      <h3 class="text-center">THE NORTH LOOP NEIGHBORHOOD ASSOCIATION <br> CLIMATE AND SUSTAINABILITY RESOLUTION </br></h3>
      <hr width="50%"></hr>
      <p>Whereas there is scientific consensus regarding the impacts of climate change both locally and globally, and</p>
      <p>Whereas these climate change impacts, if not controlled, are projected to undermine the quality of life, economy, and unique cultures of our city, and</p>
      <p>Whereas the City of Austin has adopted strategies, plans, and processes to help ensure the city achieves net-zero community-wide greenhouse gases, and</p>
      <p>Whereas greater cooperation, including environmental leadership by the State of Texas, is necessary to achieve effective climate-management goals,</p>
      <p>Be it resolved by the North Loop Neighborhood Association (NLNA) that:</p>
      <p>The NLNA is committed, as part of its core mission, to supporting efforts to reduce greenhouse emissions and to mitigate the effects of climate change.</p>
      <p>NLNA endorses the City of Austin’s strategic goals, programs, and monitoring processes aimed at helping our city reduce the effects of climate change and develop resilience in the face of climate-change threats.  Such efforts include actions in strategic areas such as:</p>
      <ul>
        <li>Decreasing energy use in new and existing buildings</li>
        <li>Eliminating fossil fuels to generate electricity and using Smart Grid technologies to deliver energy from renewable sources</li>
        <li>Increasing availability and support of mass transit, bicycle, pedestrian options, and electric vehicles, as well as phased reduction in the use of fossil-fuel-based vehicles</li>
        <li>Revising land-use codes, as appropriate, to reduce urban sprawl and commuting distances, as well as promote investment in compact, energy-efficient homes</li>
        <li>Reducing energy use and methane production in Austin’s waste management activities</li>
        <li>Managing long-term risks of climate change on already vulnerable communities, individuals experiencing poverty, and on city infrastructure, including the risks of extreme weather on power generation and delivery</li>
      </ul>
      <p>To address climate change on the state level, the NLNA urges the Texas Legislature to pass legislation providing for:</p>
      <ul>
        <li>Controls on oil and gas facilities to reduce methane and other emissions, </li>
        <li>Reduction of methane flaring and venting sufficient to protect the health of all Texans</li>
        <li>Investigation, adoption, and implementation of additional emissions-reduction best practices </li>
      </ul>
      <p>In order to proactively address climate change and promote a spirit of environmental responsibility within our community, the NLNA commits to additional necessary actions including, but not limited to:</p>
       <ul>
        <li>Creating a standing committee on climate that would coordinate with existing work groups and initiatives within the NLNA to advance climate-related goals.</li>
        <li>Working with the North Loop Neighborhood Plan Contact Team (NPCT) to review the neighborhood plan and ensure that it is both responsive to the climate-change challenges the neighborhood faces and consistent with the city’s climate-management goals. The NPCT plan includes the Austin neighborhoods of North Loop, Ridgetop, and the adjacent area known as the Red River Sliver.</li>
        <li>Committing to understand and monitor climate change issues in the community; to coordinate with relevant agencies and groups working on climate change issues; and to develop, adopt, and monitor bi-annual action plans for advancing NLNA climate-related goals and priorities.</li>
        <li>Developing strategies and initiatives to exchange information with, and influence, key local climate stakeholders, including residents, visitors, local businesses, developers, government, and other relevant organizations.</li>
      </ul>
