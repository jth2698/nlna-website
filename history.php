      <h4 class="text-center"><strong>1970s-1980s</strong></h4>
      <p>As the neighborhood filled-out, it attracted memorable restaurants and businesses. One such institution was "Scampi's Organ Palace." Scampi's was located at the southeast corner of Koenig and Lamar (the same lot currently occupied by the Goodwill / Half-Price Books strip center). For entertainment, Scampi's featured magicians, jugglers, comedians, flashing lights, balloons, bubbles, a cymbal monkey named Scampi. Scampi's centerpiece was a huge Mighty Morton theatre pipe organ, and patrons were treated to regular organ interludes as they enjoyed their pizza.</p>
      <p>Long-term residents have fond memories of Scampi's and reminisce about the pizzeria (and the monkey) to this day.</p>
