      <h4 class="text-center"><strong>1950s and 1960s - THE NEW SUBURBAN LIFESTYLE</strong></h4>
      <p>As our baby-boom generation came of age, Austin grew to the suburbs north of town.  People moved out of the city and enjoyed a lifestyle made possible by the automobile.  In the 1960s, the wide boulevard east of town known as East Avenue was torn up to make way for the new Interstate Highway 35.  A golf course owned by the Hancock family became a shopping center.  The strip-mall with the grocery store anchor became the standard form of commercial development, and the indoor shopping mall became the best way to escape the Texas heat.</p>
