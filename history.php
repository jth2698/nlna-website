      <h4 class="text-center"><strong>PRESENT DAY</strong></h4>
      <p>The rest, as they say, is history.  Austin continued to grow, doubling in size every 15 years.  The suburbs of the 1950s became part of the urban core.  Suburban-style shopping like Highland Mall and Hancock Center thrived and then decayed.  The Drive-In theater became a relic of the past, and the Chief became an office-park.  The Chicken Shack became a vacuum cleaner store, and then a Taco Cabana.  The Mueller Airport became the Mueller neighborhood, and Northfield came out of the "flight-path" and became the trendiest, coolest place to live in all of Austin.</p>
